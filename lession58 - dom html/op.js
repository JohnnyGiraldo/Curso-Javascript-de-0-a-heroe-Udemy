//¿Qué es el DOM? 
//Las siglas DOM significan Document Object 
//Model, o lo que es lo mismo, la estructura 
//del documento HTML. Una página HTML está 
//formada por múltiples etiquetas HTML, 
//anidadas una dentro de otra, formando 
//un árbol de etiquetas relacionadas entre sí,
//que se denomina árbol DOM (o simplemente DOM).

//En Javascript, cuando nos referimos al DOM 
//nos referimos a esta estructura, que podemos
//modificar de forma dinámica desde Javascript,
//añadiendo nuevas etiquetas, modificando o
//eliminando otras, cambiando sus atributos HTML, 
//añadiendo clases, cambiando el contenido de texto, etc...